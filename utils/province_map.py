def normalize_location(location: str) -> str | None:
    """
    将地点信息标准化为省份名称

    Args:
        location: 原始地点信息

    Returns:
        标准省份名称，如果无法识别则返回None
    """
    if not location or not isinstance(location, str):
        return None

    location = location.strip()

    # 精确匹配
    if location in PROVINCE_MAPPING:
        return PROVINCE_MAPPING[location]

    # 包含匹配：检查地点信息中是否包含省份关键词
    for key, value in PROVINCE_MAPPING.items():
        if key in location or location in key:
            return value

    # 无法识别
    return None


def get_all_provinces() -> list:
    """
    获取所有标准省份名称列表

    Returns:
        省份名称列表（去重）
    """
    return sorted(set(PROVINCE_MAPPING.values()))
