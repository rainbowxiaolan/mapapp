# 程度副词及权重
DEGREE_WORDS = {
    "很": 1.5, "非常": 2.0, "超级": 2.0, "特别": 1.8, "十分": 1.8,
    "极其": 2.0, "极度": 2.0, "稍微": 0.7, "有点": 0.6, "有些": 0.6,
    "不太": 0.4, "几乎": 0.3, "稍微": 0.7, "略微": 0.6
}

def get_sentiment_word_type(word: str) -> str:
    """返回 'positive', 'negative' 或 ''"""
    if word in POSITIVE_WORDS:
        return "positive"
    if word in NEGATIVE_WORDS:
        return "negative"
    return ""

def get_degree_weight(word: str) -> float:
    """返回程度副词权重，默认1.0"""
    return DEGREE_WORDS.get(word, 1.0)